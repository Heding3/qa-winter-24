package model;

public class Sun {

    private String hydrogen;
    private String helium;
    private String carbon;
    private String iron;
    private String thermonuclearReaction;

    public String getHydrogen() {
        return hydrogen;
    }

    public void setHydrogen(String hydrogen) {
        this.hydrogen = hydrogen;
    }

    public String getHelium() {
        return helium;
    }

    public void setHelium(String helium) {
        this.helium = helium;
    }

    public String getCarbon() {
        return carbon;
    }

    public void setCarbon(String carbon) {
        this.carbon = carbon;
    }

    public String getIron() {
        return iron;
    }

    public void setIron(String iron) {
        this.iron = iron;
    }

    public String getThermonuclearReaction() {
        return thermonuclearReaction;
    }

    public void setThermonuclearReaction(String thermonuclearReaction) {
        this.thermonuclearReaction = thermonuclearReaction;
    }
}